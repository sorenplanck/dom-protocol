//! Deterministic cross-platform snapshot manifest.
//!
//! Roadmap v2 Phase 1.4 — every CI matrix leg runs this binary and
//! uploads its stdout as an artifact. A final job downloads every
//! host's artifact and asserts byte-identical content. Any
//! divergence is a consensus-class cross-platform regression and
//! MUST block the merge.
//!
//! The manifest format is intentionally line-oriented + sorted so
//! the diff between two diverging hosts is human-readable. Every
//! line is deterministic in the build output, derived from
//! `dom-pmmr`, `dom-test-vectors::hash_vectors`,
//! `dom-test-vectors::serialization_vectors`, and the workspace
//! constants module.

use dom_pmmr::Pmmr;
use dom_serialization::{DomDeserialize, DomSerialize};
use dom_test_vectors::{
    hash_vectors::generate_hash_vectors, pmmr_vectors::required_pmmr_vectors,
};

/// Manifest magic + version. Bump the version when a deliberate
/// change to the snapshot format is made; doing so REQUIRES every
/// matrix leg to be rerun and the CI compare-job updated.
const MANIFEST_VERSION: &str = "1";

fn main() {
    println!("# DOM cross-platform snapshot v{MANIFEST_VERSION}");
    println!("# Generated by crates/dom-test-vectors/src/bin/cross_platform_snapshot.rs");
    println!();

    // Section 1: consensus constants.
    println!("[constants]");
    println!("INITIAL_BLOCK_REWARD={}", dom_core::INITIAL_BLOCK_REWARD);
    println!("HALVING_INTERVAL={}", dom_core::HALVING_INTERVAL);
    println!("HALVING_EPOCHS={}", dom_core::HALVING_EPOCHS);
    println!("TARGET_SPACING={}", dom_core::TARGET_SPACING);
    println!("MAX_SUPPLY_NOMS={}", dom_core::MAX_SUPPLY_NOMS);
    println!("COINBASE_MATURITY={}", dom_core::COINBASE_MATURITY);
    println!("MAX_FUTURE_BLOCK_TIME={}", dom_core::MAX_FUTURE_BLOCK_TIME);
    println!("NETWORK_MAGIC_MAINNET={:#010x}", dom_core::NETWORK_MAGIC_MAINNET);
    println!("NETWORK_MAGIC_TESTNET={:#010x}", dom_core::NETWORK_MAGIC_TESTNET);
    println!("P2P_PORT_MAINNET={}", dom_core::P2P_PORT_MAINNET);
    println!("PROTOCOL_VERSION={}", dom_core::PROTOCOL_VERSION);
    println!("ASERT_HALF_LIFE={}", dom_core::ASERT_HALF_LIFE);
    println!("ASERT_RADIX_BITS={}", dom_core::ASERT_RADIX_BITS);
    println!("MAX_BLOCK_WEIGHT={}", dom_core::MAX_BLOCK_WEIGHT);
    println!("MAX_TX_WEIGHT={}", dom_core::MAX_TX_WEIGHT);
    println!("MAX_INPUTS_PER_TX={}", dom_core::MAX_INPUTS_PER_TX);
    println!("MAX_OUTPUTS_PER_TX={}", dom_core::MAX_OUTPUTS_PER_TX);
    println!("MAX_KERNELS_PER_TX={}", dom_core::MAX_KERNELS_PER_TX);
    println!("MAX_PROOF_SIZE={}", dom_core::MAX_PROOF_SIZE);
    println!("MAX_HEADERS_PER_MSG={}", dom_core::MAX_HEADERS_PER_MSG);
    println!();

    // Section 2: PMMR roots — the RFC-0004 canonical set.
    println!("[pmmr_roots]");
    for v in required_pmmr_vectors() {
        let mut pmmr = Pmmr::new();
        for i in 0..v.leaf_count {
            pmmr.push(&(i as u64).to_le_bytes())
                .expect("push must succeed");
        }
        println!("n={} root={}", v.leaf_count, pmmr.root().to_hex());
    }
    println!();

    // Section 3: hash-vector outputs.
    println!("[hash_vectors]");
    let mut hashes = generate_hash_vectors();
    hashes.sort_by(|a, b| a.0.cmp(&b.0));
    for (desc, hex) in hashes {
        println!("{desc}={hex}");
    }
    println!();

    // Section 4: serialization round-trip primitives — pin the
    // exact byte sequences for a curated set of canonical values.
    println!("[serialization]");
    for value in [0u64, 1, 33, 1_000_000_000, dom_core::MAX_SUPPLY_NOMS] {
        let amount = dom_core::Amount::from_noms(value).expect("amount");
        let mut writer = dom_serialization::Writer::default();
        amount.serialize(&mut writer).expect("ser");
        let bytes = writer.finish();
        println!("Amount({value})={}", hex::encode(&bytes));
        // Round-trip — value MUST decode back to the same number.
        let mut reader = dom_serialization::Reader::new(&bytes);
        let decoded = dom_core::Amount::deserialize(&mut reader).expect("de");
        assert_eq!(decoded.noms(), value, "Amount roundtrip failed");
    }
    for h in 0u64..=4 {
        let height = dom_core::BlockHeight(h);
        let mut writer = dom_serialization::Writer::default();
        height.serialize(&mut writer).expect("ser");
        println!("BlockHeight({h})={}", hex::encode(writer.finish()));
    }
    println!();

    // Section 5: empty-PMMR + single-leaf identity probes — these
    // exercise the tagged Blake2b domain and the leaf-hash path
    // independently of the multi-leaf MMR layout.
    println!("[crypto_identities]");
    println!(
        "empty_pmmr_root={}",
        Pmmr::new().root().to_hex()
    );
    let one_leaf_hash = dom_pmmr::leaf_hash(1, b"DOM/cross_platform/v1");
    println!("leaf_hash(1, DOM/cross_platform/v1)={}", one_leaf_hash.to_hex());
    let g_left = dom_core::Hash256::from_bytes([0x11u8; 32]);
    let g_right = dom_core::Hash256::from_bytes([0x22u8; 32]);
    let node = dom_pmmr::node_hash(3, &g_left, &g_right);
    println!("node_hash(3, 0x11.., 0x22..)={}", node.to_hex());
    println!();

    // Trailer — explicit so a future format-version change is
    // self-evident in diffs.
    println!("# end-of-manifest");
}
